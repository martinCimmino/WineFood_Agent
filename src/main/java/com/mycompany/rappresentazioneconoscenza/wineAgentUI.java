    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (isSugarSelected == false && isColorSelected == false && isFlavorSelected == false && isBodySelected == false ) {
             jDialog1.setVisible(true);  
        } else {
            if (!isSugarSelected) {
                sugar_prop = null;
            }
            if (!isColorSelected) {
                color_prop = null;
            }
            if (!isFlavorSelected) {
                flavor_prop = null;
            }
            if (!isBodySelected) {
                body_prop = null;
            }
            wine = agent.lookupWine(sugar_prop, color_prop, flavor_prop, body_prop);
            jLabel3.setText("We suggest the following wines: ");
            jLabel3.setVisible(true);
            outputMode = true;
            DefaultListModel modelOutput = new DefaultListModel();
            //jList1 = new JList(model);
            for (int i = 0; i < wine.length; i++) {
                modelOutput.addElement(wine[i]);
            }
            jList1.setModel(modelOutput);
            jScrollPane1.setViewportView(jList1);
            jScrollPane1.setVisible(true);
            jButton5.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

