    private void jList1MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseEntered
        // TODO add your handling code here:
        if(!outputMode){
            jList1.clearSelection();    
            String[] arr = wineFoodAgent.getDishInstancesArr();
            modelInput = new DefaultListModel();
            //jList1 = new JList(model);
            for (int i = 0; i < arr.length; i++) {
                modelInput.addElement(arr[i]);
            }
            jList1.setModel(modelInput);
        } 
    }//GEN-LAST:event_jList1MouseEntered

    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        if (jList1.getSelectedValue() != null && !outputMode ){
            jScrollPane1.setViewportView(null);
            jScrollPane1.setVisible(false);
            jRadioButton1.setVisible(true);
            jRadioButton2.setVisible(true);
            jRadioButton3.setVisible(true);
            jRadioButton4.setVisible(true);
            jButton5.setVisible(true);
            jLabel2.setText("For "+ jList1.getSelectedValue() + " we suggest a wine with the following properties");
            Map dictionary = agent.lookUpWineProperties(jList1.getSelectedValue());

            if (dictionary.containsKey("hasSugar")){
                jRadioButton1.setEnabled(true);
                sugar_prop = (String) dictionary.get("hasSugar");
                jRadioButton1.setText("Sugar: "+ sugar_prop);  
            } else {
                jRadioButton1.setText("No Sugar Property found"); 
                jRadioButton1.setEnabled(false);
            }

            if (dictionary.containsKey("hasColor")){
                jRadioButton2.setEnabled(true);
                color_prop = (String) dictionary.get("hasColor");
                jRadioButton2.setText("Color: "+ color_prop);  
            } else {
                jRadioButton2.setText("No Color Property found"); 
                jRadioButton2.setEnabled(false);
            }

            if (dictionary.containsKey("hasFlavor")){
                jRadioButton3.setEnabled(true);
                flavor_prop = (String) dictionary.get("hasFlavor");
                jRadioButton3.setText("Flavor: "+ flavor_prop);  
            } else {
                jRadioButton3.setText("No Flavor Property found"); 
                jRadioButton3.setEnabled(false);
            }
            if (dictionary.containsKey("hasBody")){
                jRadioButton4.setEnabled(true);
                body_prop = (String) dictionary.get("hasBody");
                jRadioButton4.setText("Body: "+ body_prop);  
            } else {
                jRadioButton4.setText("No Body Property found"); 
                jRadioButton4.setEnabled(false);
            }
        }

        //System.exit(0);
    }//GEN-LAST:event_jList1ValueChanged
    
